$('.project-main6-from button').click(function(){
		var Hname = $('.project-main6-from input').eq(0).val();
		var Hphone = $('.project-main6-from input').eq(1).val();
		var Nchina = /^[\u4E00-\u9FA5]{1,5}$/;//中文
		// 姓名
        if (Hname == '') {
        	alert('姓名不能为空！');
        	return
        }else if(Nchina.test(Hname)){
            // alert('ok');
        }else{
        	alert('请填写中文姓名!');
        	return
        }
        // 手机号
        if (Hphone == '') {
        	alert('手机号不能为空！');
        	return
        }else if(!(/^1[34578]\d{9}$/.test(Hphone))){
            alert('手机号有误！');
        }
})
